#include<iostream>
using namespace std;
int main()
{
	
		cout <<  "bool类型数据的长度为" << sizeof(bool) << "字节"<<endl;
		cout << "char类型数据的长度为" << sizeof(char) << "字节" << endl;
		cout << "unsigned char类型数据的长度为" << sizeof(unsigned char) << "字节" << endl;
		cout << "short类型数据的长度为" << sizeof(short) << "字节" << endl;
		cout << "unsigned short类型数据的长度为" << sizeof(unsigned short) << "字节" << endl;
		cout << "int类型数据的长度为" << sizeof(int) << "字节" << endl;
		cout << "unsigned int类型数据的长度为" << sizeof(unsigned int) << "字节" << endl;
		cout << "long类型数据的长度为" << sizeof(long) << "字节" << endl;
		cout << "float类型数据的长度为" << sizeof(float) << "字节" << endl;
		cout << "double类型数据的长度为" << sizeof(double)<< "字节" << endl;
		cout << "long double类型数据的长度为" << sizeof(long double) << "字节" << endl;

	return 0;
}
